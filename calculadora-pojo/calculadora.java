public class calculadora {
    public int suma (int a, int b) {
        return a + b;
    }
    
    public int resta (int a, int b) {
        return a - b;
    }
    
    public int multiplcia (int a, int b) {
        return a * b;
    }
    
    public int divide (int a, int b) {
        return a / b;
    }
}